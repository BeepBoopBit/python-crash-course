places = ['water', 'melon', 'papaya', 'banana', 'fruit salad']

print(places)

# Print in sorted order without modifying the list
print(sorted(places))

places.reverse()

# print the reverse order of the places list
print(places)

# re-reverse it back
places.reverse()

print(places)

# sort the place
places.sort()

#print the place
print(places)

# sort the place in reverse order
